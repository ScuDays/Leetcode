class Solution {
public:
    vector<int> intersection(vector<int>& nums1, vector<int>& nums2) {
        unordered_set<int> IntSet;
        unordered_set<int> rtnSet;
        vector<int> rtnVector;
        for(int i = 0; i < nums1.size(); i++){
            IntSet.insert(nums1[i]);
        }
        for(int i = 0; i < nums2.size(); i++){
            if(IntSet.count(nums2[i]) != 0){
                rtnSet.insert(nums2[i]);
            }
        }
        for(auto i: rtnSet){
            rtnVector.push_back(i);
        }
        return rtnVector;

    }
};
