class Solution
{
public:
    string reverseWords(string s)
    {
        vector<string> strVect;
        for (int i = 0; i < s.size(); i++)
        {
            string insert;
            while (s[i] == ' ' && i < s.size())
                i++;
            while (s[i] != ' ' && i < s.size())
            {
                insert += s[i];
                i++;
            }
            if (insert.size() != 0)
            {
                strVect.push_back(insert);
            }
        }
        string rtn;
        for (int i = strVect.size() - 1; i >= 0; i--)
        {
            rtn += strVect[i];
            if (i == 0)
                break;
            rtn += ' ';
        }
        printf("%s\n", rtn.c_str());
        printf("%d\n", strVect.size());
        return rtn;
    }
};
