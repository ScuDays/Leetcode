class MyQueue {
public:
        stack<int> pushStack;
        stack<int> popStack;
    MyQueue() {

    }
    
    void push(int x) {
        pushStack.push(x);
    }   
    
    int pop() {
        if(popStack.empty()){
            int size = pushStack.size();
            for(int i = 0; i < size; i ++){
                int ele = pushStack.top();
                popStack.push(ele);
                pushStack.pop();
            }
        }
        int rtn = popStack.top();  
        popStack.pop();
        return rtn;
    }
    
    int peek() {    
        int ele = pop();
        popStack.push(ele);
        return ele;
    }
    
    bool empty() {
        int size = popStack.size() + pushStack.size();
        if(size == 0){
            return true;
        }
        return false;
    }
};

